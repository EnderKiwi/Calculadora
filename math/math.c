#include "math.h"

int end_add(int a, int b)
{
     return a + b;
}

int end_sub(int a, int b)
{
     return a - b;
}

int end_mul(int a, int b)
{
     return a * b;
}

int end_div(int a, int b)
{
     // TODO: Check for division by zero
     return a / b;
}

int end_mod(int a, int b)
{
     return a % b;
}
